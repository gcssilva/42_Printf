#include "ft_printf.h"

int	ft_printf_s(char *s)
{
	int		i;

	if (!s)
	{
		ft_putstr_fd("(null)", 1);
		return (6);
	}
	i = -1;
	while (s[++i])
		ft_putchar_fd(s[i], 1);
	return (i);
}
